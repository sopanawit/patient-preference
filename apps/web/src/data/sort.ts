/**
 * เปรียบเทียบเลขห้องพักแบบรับรู้ตัวเลข (natural sort) เพื่อให้เรียงจากน้อยไปมาก
 * ได้ถูกต้อง เช่น "205" มาก่อน "301/A" และ "5" มาก่อน "10"
 */
export function compareRooms(a: string, b: string): number {
  return a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" });
}
